function cartCount() {
    let cart = document.getElementById('cart-count');
    if (sessionStorage.getItem('item') !== null) {
        let s = sessionStorage.getItem('item').split(',');
        cart.innerHTML = s.length - 1;
    }
}

function generateCart() {
    let cartImage = document.getElementsByClassName('cart-item-img');
    let cartName = document.getElementsByClassName('cart-item-name');
    let cartPrice = document.getElementsByClassName('cart-item-price');
    let cartTotal = document.getElementById('cart-total-price');
    let cartItems = document.getElementById('cart-items');

    if (sessionStorage.getItem('item') !== null) {
        let s = sessionStorage.getItem('item').split(',');

        let total = 0;

        for (let a = 1; a < s.length; ++a) {
            let index = item.indexOf(s[a]);
            cartItems.innerHTML +=`<div class="cart-item"><div class="cart-item-img" style="background-image: url('${itemImage[index][0]}');"></div><div class="cart-item-name">${item[index]}</div><div class="cart-item-price">&#8377;${itemPrice[index]}</div></div>`;
            total += itemPrice[index];
        }

        cartTotal.innerHTML = `Total: &#8377; ${total}`;
    }

    document.getElementById('checkout-button').onclick = function() {
        alert('The checkout Service is not available right now, Thank You!');
        sessionStorage.clear();
        document.location.href = './index.html';
    }
}





